package com.solomong.ingredient.vo;

public class IngredientVO {

	private int ingredientId;
	private String ingredientName;
	private int ingredientType;
	private int ingredientUseByPeriod;

	public int getIngredientId() {
		return ingredientId;
	}

	public void setIngredientId(int ingredientId) {
		this.ingredientId = ingredientId;
	}

	public String getIngredientName() {
		return ingredientName;
	}

	public void setIngredientName(String ingredientName) {
		this.ingredientName = ingredientName;
	}

	public int getIngredientType() {
		return ingredientType;
	}

	public void setIngredientType(int ingredientType) {
		this.ingredientType = ingredientType;
	}

	public int getIngredientUseByPeriod() {
		return ingredientUseByPeriod;
	}

	public void setIngredientUseByPeriod(int ingredientUseByPeriod) {
		this.ingredientUseByPeriod = ingredientUseByPeriod;
	}

}
